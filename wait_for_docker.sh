echo "--- Начинаю проверку статуса Docker ---"

# Цикл будет работать до тех пор, пока команда "systemctl status docker"
# завершается с кодом НЕ 0 (т.е., пока Docker НЕ активен)
while ! systemctl status docker > /dev/null 2>&1; do
    
    # Этот блок выполняется, если Docker не активен
    echo "❌ Docker не активен. Ждем 5 секунд..."
    sleep 5
    
done # Закрытие цикла

# Если цикл завершился, значит, условие стало ложным (Docker активен).
echo "✅ Docker успешно запущен! Продолжаем работу."

exit 0
