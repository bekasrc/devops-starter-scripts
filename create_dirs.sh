#!/bin/bash

# $1, $2, $3, ... - это сервисы, которые мы передали

if [ $# -lt 1 ]; then
echo "Укажите название файлы"
exit 1
fi
for SERVICE in "$@"; do
mkdir "SERVICE"
if [ $? -eq 0 ]; then
    # Проверяем код завершения последней команды  
echo "--- Запуск создания папок ---"
echo "✅ Создана папка: $@ "
echo "✅ Все папки успешно созданы."
fi
done
exit 0
